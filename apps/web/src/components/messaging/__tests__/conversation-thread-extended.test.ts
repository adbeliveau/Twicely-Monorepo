/**
 * Extended logic tests for ConversationThread — additional edge cases.
 */
import { describe, it, expect } from 'vitest';
import type { ConversationDetail, MessageItem } from '@/lib/queries/messaging';

// ─── Pure logic extracted from ConversationThread ─────────────────────────────

function getListingHref(slug: string | null): string | null {
  if (!slug) return null;
  return `/i/${slug}`;
}

function shouldShowListingContextCard(slug: string | null): boolean {
  return slug !== null;
}

function shouldShowListingPrice(priceCents: number | null): boolean {
  return priceCents !== null;
}

function shouldShowListingImage(imageUrl: string | null): boolean {
  return imageUrl !== null;
}

function shouldShowOrderLinkedBadge(orderId: string | null): boolean {
  return orderId !== null;
}

function getAvatarInitial(senderName: string): string {
  return senderName.charAt(0).toUpperCase();
}

function isSystemMessage(msg: MessageItem): boolean {
  return msg.isAutoGenerated;
}

function isOwnMessage(senderUserId: string, currentUserId: string): boolean {
  return senderUserId === currentUserId;
}

function shouldShowCompose(status: ConversationDetail['status']): boolean {
  return status === 'OPEN';
}

function getSubjectDisplay(subject: string | null): string {
  return subject ?? 'Conversation';
}

function getFlaggedBannerText(): string {
  return 'For your protection, complete transactions on Twicely.';
}

function getClosedBannerText(): string {
  return 'This conversation is closed.';
}

// ─── Test Data ────────────────────────────────────────────────────────────────

const BUYER_ID = 'user-buyer-001';
const SELLER_ID = 'user-seller-001';

function makeConversation(overrides: Partial<ConversationDetail> = {}): ConversationDetail {
  return {
    id: 'conv-test-001',
    listingId: 'listing-test-001',
    orderId: null,
    buyerId: BUYER_ID,
    sellerId: SELLER_ID,
    subject: 'Vintage Camera',
    status: 'OPEN',
    isFlagged: false,
    createdAt: new Date('2026-01-01'),
    listingTitle: 'Vintage Camera',
    listingSlug: 'vintage-camera-abc',
    listingPriceCents: 4999,
    listingImageUrl: 'https://cdn.example.com/camera.jpg',
    otherPartyId: SELLER_ID,
    otherPartyName: 'Bob Seller',
    otherPartyImage: null,
    ...overrides,
  };
}

function makeMessage(overrides: Partial<MessageItem> = {}): MessageItem {
  return {
    id: 'msg-test-001',
    senderUserId: BUYER_ID,
    senderName: 'Alice Buyer',
    body: 'Is this available?',
    attachments: [],
    isRead: true,
    readAt: new Date(),
    isAutoGenerated: false,
    createdAt: new Date('2026-01-10'),
    ...overrides,
  };
}

// ─── Listing context card ─────────────────────────────────────────────────────

describe('ConversationThread — listing context card', () => {
  it('shows listing card when listingSlug is present', () => {
    const conv = makeConversation({ listingSlug: 'vintage-camera-abc' });
    expect(shouldShowListingContextCard(conv.listingSlug)).toBe(true);
  });

  it('hides listing card when listingSlug is null', () => {
    const conv = makeConversation({ listingSlug: null });
    expect(shouldShowListingContextCard(conv.listingSlug)).toBe(false);
  });

  it('listing link points to /i/:slug route', () => {
    expect(getListingHref('vintage-camera-abc')).toBe('/i/vintage-camera-abc');
  });

  it('listing link returns null when slug is null', () => {
    expect(getListingHref(null)).toBeNull();
  });

  it('shows price when listingPriceCents is not null', () => {
    const conv = makeConversation({ listingPriceCents: 4999 });
    expect(shouldShowListingPrice(conv.listingPriceCents)).toBe(true);
  });

  it('hides price when listingPriceCents is null', () => {
    const conv = makeConversation({ listingPriceCents: null });
    expect(shouldShowListingPrice(conv.listingPriceCents)).toBe(false);
  });

  it('shows listing image when imageUrl is present', () => {
    const conv = makeConversation({ listingImageUrl: 'https://cdn.example.com/img.jpg' });
    expect(shouldShowListingImage(conv.listingImageUrl)).toBe(true);
  });

  it('hides listing image when imageUrl is null', () => {
    const conv = makeConversation({ listingImageUrl: null });
    expect(shouldShowListingImage(conv.listingImageUrl)).toBe(false);
  });
});

