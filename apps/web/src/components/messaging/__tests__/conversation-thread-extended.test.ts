// ─── Message bubble alignment and avatar ────────────────────────────────────

describe('ConversationThread — message bubble alignment', () => {
  it('own message aligns right (senderUserId === currentUserId)', () => {
    expect(isOwnMessage(BUYER_ID, BUYER_ID)).toBe(true);
  });

  it('other party message aligns left (senderUserId !== currentUserId)', () => {
    expect(isOwnMessage(SELLER_ID, BUYER_ID)).toBe(false);
  });

  it('auto-generated message is NOT treated as own message', () => {
    const msg = makeMessage({ senderUserId: 'system', isAutoGenerated: true });
    expect(isSystemMessage(msg)).toBe(true);
  });

  it('normal buyer message is not a system message', () => {
    const msg = makeMessage({ senderUserId: BUYER_ID, isAutoGenerated: false });
    expect(isSystemMessage(msg)).toBe(false);
  });
});

// ─── Avatar initial extraction ────────────────────────────────────────────────

describe('ConversationThread — avatar initial', () => {
  it('extracts first character uppercased for "Alice"', () => {
    expect(getAvatarInitial('Alice Buyer')).toBe('A');
  });

  it('extracts first character uppercased for "bob" (lowercase)', () => {
    expect(getAvatarInitial('bob')).toBe('B');
  });

  it('handles single character name', () => {
    expect(getAvatarInitial('Z')).toBe('Z');
  });

  it('returns uppercase initial for lowercase name', () => {
    expect(getAvatarInitial('john doe')).toBe('J');
  });
});

// ─── Message list edge cases ──────────────────────────────────────────────────

describe('ConversationThread — message list edge cases', () => {
  it('handles empty messages array gracefully', () => {
    const messages: MessageItem[] = [];
    expect(messages.length).toBe(0);
  });

  it('handles conversation with only auto-generated messages', () => {
    const messages = [
      makeMessage({ isAutoGenerated: true, senderUserId: 'system', senderName: 'System' }),
    ];
    expect(messages.every((m) => m.isAutoGenerated)).toBe(true);
  });

  it('handles mix of own, other, and system messages', () => {
    const messages = [
      makeMessage({ senderUserId: BUYER_ID, isAutoGenerated: false }),
      makeMessage({ senderUserId: SELLER_ID, isAutoGenerated: false }),
      makeMessage({ senderUserId: 'system', isAutoGenerated: true }),
    ];
    const ownCount = messages.filter((m) => isOwnMessage(m.senderUserId, BUYER_ID)).length;
    const otherCount = messages.filter(
      (m) => !isOwnMessage(m.senderUserId, BUYER_ID) && !m.isAutoGenerated,
    ).length;
    const systemCount = messages.filter((m) => m.isAutoGenerated).length;
    expect(ownCount).toBe(1);
    expect(otherCount).toBe(1);
    expect(systemCount).toBe(1);
  });
});
